async function createGroups(companyId: string, groups: CategoryGroup[], type: "EXPENSE" | "INCOME") {
  for (let i = 0; i < groups.length; i++) {
    const group = groups[i];
    const parent = await prisma.transactionCategory.create({
      data: { companyId, name: group.name, type, parentId: null, sortOrder: i },
    });
    for (let j = 0; j < group.children.length; j++) {
      await prisma.transactionCategory.create({
        data: { companyId, name: group.children[j], type, parentId: parent.id, sortOrder: j },
      });
    }
  }
}

export async function seedCategoriesForCompany(companyId: string) {
  await createGroups(companyId, EXPENSE_CATEGORIES, "EXPENSE");
  await createGroups(companyId, INCOME_CATEGORIES, "INCOME");
}

