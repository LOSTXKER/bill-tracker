export async function seedAccounts(companyId: string) {
  console.log(`\nðŸŒ± Seeding default Chart of Accounts for company: ${companyId}`);

  let created = 0;
  let skipped = 0;

  for (const accountData of DEFAULT_ACCOUNTS) {
    try {
      // Check if account already exists
      const existing = await prisma.account.findUnique({
        where: {
          companyId_code: {
            companyId,
            code: accountData.code,
          },
        },
      });

      if (existing) {
        console.log(`  â­ï¸  Account ${accountData.code} (${accountData.name}) already exists`);
        skipped++;
        continue;
      }

      // Create account
      await prisma.account.create({
        data: {
          companyId,
          code: accountData.code,
          name: accountData.name,
          class: accountData.class,
          keywords: accountData.keywords,
          description: accountData.description,
          isSystem: true,
          isActive: true,
        },
      });

      console.log(`  âœ… Created account ${accountData.code} - ${accountData.name}`);
      created++;
    } catch (error) {
      console.error(`  âŒ Error creating account ${accountData.code}:`, error);
    }
  }

  console.log(`\nðŸ“Š Summary: ${created} created, ${skipped} skipped\n`);
  return { created, skipped };
}

// Run as standalone script
async function main() {
  console.log("ðŸš€ Starting Chart of Accounts Seed");

  // Get all companies
  const companies = await prisma.company.findMany({
    select: { id: true, code: true, name: true },
  });

  if (companies.length === 0) {
    console.log("âš ï¸  No companies found. Please create a company first.");
    return;
  }

  console.log(`Found ${companies.length} companies:`);
  companies.forEach((c) => console.log(`  - ${c.code}: ${c.name}`));

  // Seed accounts for each company
  for (const company of companies) {
    await seedAccounts(company.id);
  }

  console.log("âœ¨ Chart of Accounts seed completed!");
}

